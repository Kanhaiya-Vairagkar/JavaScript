// The Date object works with dates and times.
// Date objects are created with new Date().
// There are four ways of instantiating (creating) a date

var r = new Date();
console.log(r.getDate());
console.log(r.getFullYear());
console.log(r.getTime());
console.log(r.getMilliseconds());
console.log(r.getMinutes());
console.log(r.getHours());
console.log(r.getDay());